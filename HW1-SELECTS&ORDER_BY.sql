	COD_SERIE NUMBER(2) REFERENCES SERII(COD_SERIE));
CREATE TABLE STUDENT
	(ID_STUD NUMBER(13) PRIMARY KEY,
	DENUMIRE VARCHAR2(100),
	COD_GRUPA NUMBER(2) REFERENCES GRUPE(COD_GRUPA));

